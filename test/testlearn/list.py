# 增
a = ['a','b','c','d']
a.append('3')
print (a) #-->['a', 'b', 'c', 'd', '3']


# 删
a = ['a','b','c','d',1]
del a[3]  #通过下标删除
print (a) #-->['a', 'b', 'c']

# 改
a = ['a','b','c','d']
a[3] = 10
print (a) #-->['a', 'b', 'c', 10]

#查
a = ['a','b','c','d']
print (a[-1]) #--> d



#抛出最后一个，等于删除最后一个？
a = ['a','b','c','d']
print (a.pop())#--> d
print (a)#--> ['a', 'b', 'c']
